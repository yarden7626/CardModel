package com.example.cardmodel2;

public class CardModel
{
    String text;
    int image;

    public CardModel(String text, int image)
    {
        this.text=text;
        this.image=image;
    }
    public String GetText()
    {
        return text;
    }
    public int GetImage()
    {
        return image;
    }







}
